cat <<'EOF'
_____/\\\\\\\\\\\____/\\\______________/\\\________/\\\__/\\\\\\\\\\\\________/\\\\\\\\\\\\__/\\\\\\\\\\\\\\\_        
 ___/\\\/////////\\\_\/\\\_____________\/\\\_______\/\\\_\/\\\////////\\\____/\\\//////////__\/\\\///////////__       
