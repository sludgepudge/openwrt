   ___\////\\\_________\/\\\_____________\/\\\_______\/\\\_\/\\\_______\/\\\_\/\\\____/\\\\\\\_\/\\\\\\\\\\\_____     
    ______\////\\\______\/\\\_____________\/\\\_______\/\\\_\/\\\_______\/\\\_\/\\\___\/////\\\_\/\\\///////______    
     _________\////\\\___\/\\\_____________\/\\\_______\/\\\_\/\\\_______\/\\\_\/\\\_______\/\\\_\/\\\_____________   
      __/\\\______\//\\\__\/\\\_____________\//\\\______/\\\__\/\\\_______/\\\__\/\\\_______\/\\\_\/\\\_____________  
       _\///\\\\\\\\\\\/___\/\\\\\\\\\\\\\\\__\///\\\\\\\\\/___\/\\\\\\\\\\\\/___\//\\\\\\\\\\\\/__\/\\\\\\\\\\\\\\\_ 
        ___\///////////_____\///////////////_____\/////////_____\////////////______\////////////____\///////////////__
EOF

# Info below the ASCII
echo -e "${YELLOW}"
echo "-----------------------------------------------------"
echo -e " Version: ${WHITE}${DISTRIB_REVISION}${YELLOW}"
echo -e " Target: ${WHITE}${DISTRIB_TARGET}${YELLOW}"
echo    "-----------------------------------------------------"
echo -e "${RESET}"
